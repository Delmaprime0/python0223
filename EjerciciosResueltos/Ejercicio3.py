V1 = int(input("Digite su primer valor: "))
V2 = int(input("Digite su segundo valor: "))
V3 = int(input("Digite su tercer valor: "))

suma = V1 + V2 + V3
resta = V1 - V2 - V3
multiplicacion = V1 * V2 * V3
div = V1 / V2 / V3
div_entera = V1 // V2 //V3

print("La suma resultante es:", suma)
print("La resta resultante es:", resta)
print("La multiplicacion resultante es:", multiplicacion)
print("La division resultante es:", div)
print("La division entera resultante es:", div_entera)